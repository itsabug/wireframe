// Generate time series data for protocols
const generateTimeSeries = (baseIn: number, baseOut: number, variance: number = 0.3) => {
  const points = [];
  const times = ['0:00', '0:30', '1:00', '1:30', '2:00', '2:30', '3:00', '3:30', '4:00', '4:30', '5:00', '5:30'];
  
  for (const time of times) {
    const inVariance = 1 + (Math.random() - 0.5) * variance;
    const outVariance = 1 + (Math.random() - 0.5) * variance;
    points.push({
      time,
      inbound: Math.round(baseIn * inVariance),
      outbound: Math.round(baseOut * outVariance),
    });
  }
  return points;
};

